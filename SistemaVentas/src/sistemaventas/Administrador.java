    public void listar(){
        String sql = "select * from producto";
        try {
            cn=Conexion.GetConnection();
            st= cn.createStatement();
            rs=st.executeQuery(sql);
            Object[] producto2 = new Object[4];
            modelo=(DefaultTableModel)TableProduct.getModel();
            while(rs.next()){
                producto2[0] = rs.getInt("idproducto");
                producto2[1] = rs.getString("descripcion");
                producto2[2] = rs.getInt("stock");
                producto2[3] = rs.getFloat("precio");
                modelo.addRow(producto2);
            }
            TableProduct.setModel(modelo);
            
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    
