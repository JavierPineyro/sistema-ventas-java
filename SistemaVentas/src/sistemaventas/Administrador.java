    private void btn_agregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregarMouseClicked
        String descripcion;
        int cantidad;
        float precio;
        boolean resultado;
        String cantidadTest, precioTest, idProductoCompra;
        
        idProductoCompra = txt_id.getText();
        descripcion = txt_descripcion.getText();
        cantidadTest = txt_cantidad.getText();
        precioTest = txt_precio.getText();
        
        if(("".equals(descripcion) || "".equals(cantidadTest) || "".equals(precioTest)) || !"".equals(idProductoCompra)){
            JOptionPane.showMessageDialog(null, "Debe Completar Todos los campos o Borrar el campo ID");
        }else{
            cantidad = Integer.parseInt(cantidadTest);
            precio = Float.parseFloat(precioTest);
            
            ControladorProducto ctrProducto = new ControladorProducto();
            Producto product = new Producto(descripcion, cantidad, precio);
            resultado = ctrProducto.guardar(product);
            if(resultado == true){
                JOptionPane.showMessageDialog(null, "El Producto Ha Sido Agregado");
                listar();
            }else{
                JOptionPane.showMessageDialog(null, "Ha Ocurrido un Error Agregando el Producto");
            }
            txt_id.setText("");
            txt_descripcion.setText("");
            txt_cantidad.setText("");
            txt_precio.setText("");
            limpiarTabla();
            listar();
        }
        
    }//GEN-LAST:event_btn_agregarMouseClicked

    private void TableProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableProductMouseClicked
        
        int fila = TableProduct.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "Producto no seleccionado");
        }else{
            id = Integer.parseInt((String)TableProduct.getValueAt(fila, 0).toString());
            String descripcion = (String)TableProduct.getValueAt(fila, 1);
            int cantidad = Integer.parseInt((String)TableProduct.getValueAt(fila, 2).toString());
            float precio = Float.parseFloat((String)TableProduct.getValueAt(fila, 3).toString());
            
            txt_id.setText(""+id);
            txt_descripcion.setText(descripcion);
            txt_cantidad.setText(""+cantidad);
            txt_precio.setText(""+precio);
        }      
    }//GEN-LAST:event_TableProductMouseClicked

